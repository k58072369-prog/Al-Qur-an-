// ─── المصحف المدني برواية حفص 612 صفحة ────────────────────
// نسخة أوسع - كل صفحة ~15 سطر بدلاً من 15 (هامش أوسع)
// نستخدم نسبة 612/604 ≈ 1.013 للتحويل التقريبي
const buildScaledPages = (
  base: Record<number, [number, number]>,
  totalBase: number,
  totalNew: number
): Record<number, [number, number]> => {
  const ratio = totalNew / totalBase;
  const result: Record<number, [number, number]> = {};
  for (const id in base) {
    const [s, e] = base[id];
    result[id] = [
      Math.max(1, Math.round(s * ratio)),
      Math.min(totalNew, Math.round(e * ratio)),
    ];
  }
  return result;
};

